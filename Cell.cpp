#include "Cell.h"

//======================================================================
//                  DEFINITION STATIC ATTRIBUTES
//======================================================================
  const float Cell::fitness_min_ = 0.001;
  const float Cell::Pdeath_ = 0.02; 
  const float Cell::Pmut_ = 0.001;
  const float Cell::dt_ = 0.1;
  int Cell::nb_cell = 0;
  
//======================================================================
//                          CONSTRUCTORS
//======================================================================
Cell::Cell()
  { 
    fitness_ = 0;
    concentrations_ = {0.0,0.0,0.0};
    nb_cell++;
  }

Cell::Cell(vector<float> intra_metabolites)
  {
    fitness_ = 0;
    concentrations_= intra_metabolites;
    nb_cell++;
  }

//======================================================================
//                         DESTRUCTOR
//======================================================================
Cell::~Cell()
  {
	nb_cell--;
  }
  
//======================================================================
//                        PUBLIC METHODS
//======================================================================
void Cell::Cell_division()
  {
    concentrations_[0] /= 2;
    concentrations_[1] /= 2;
    concentrations_[2] /= 2;
  }
  
//======================================================================
//                           GETTERS
//======================================================================
vector<float> Cell::intra_metabolites()
{  
  return(concentrations_);
}
